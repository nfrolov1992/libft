#include "libft.h"

void		ft_putnbr(int n)
{
	char c;

	c = '0';
	if (n < 0)
	{
		if (n == INT32_MIN)
		{
			write(1, "-2147483648", 11);
			return ;
		}
		write(1, "-", 1);
		n *= -1;
	}
	if (n < 10)
	{
		c += n;
		write(1, &c, 1);
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
